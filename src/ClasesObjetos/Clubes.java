package ClasesObjetos;

public class Clubes {
	private String nombre;

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	private String estadioNombre;
	
	public String getEstadioNombre() {
		return estadioNombre;
	}

	public void setEstadioNombre(String estadioNombre) {
		this.estadioNombre = estadioNombre;
	}

	private String direccionEstadio;

	public String getDireccionEstadio() {
		return direccionEstadio;
	}

	public void setDireccionEstadio(String direccionEstadio) {
		this.direccionEstadio = direccionEstadio;
	}
	
	private int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	private String escudoRuta;

	public String getEscudoRuta() {
		return escudoRuta;
	}

	public void setEscudoRuta(String escudoRuta) {
		this.escudoRuta = escudoRuta;
	}
}
